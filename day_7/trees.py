# Tree ek hierarchical data structure hai jisme nodes parent-child relationship mein connected hote hain.
# (A tree is a hierarchical data structure made up of nodes connected by edges, 
# with one node designated as the root and no cycles present.)
# Key Characteristics--

# Has one root node.
# Each node can have zero or more children.
# Nodes are connected by edges.
# There are no cycles in a tree.
# A tree with n nodes has n − 1 edges.

# example- 
#       A
#      / \
#     B   C
#    / \
#   D   E

# A → Root node
# B, C → Children of A
# D, E → Children of B
# D, E, C → Leaf nodes
